function countDistricts(a){
  const r=[],u=new Set()
  const dfs=x=>{
    let n=r.length-1
    if(r[n].has(x)||u.has(x))return
    r[n].add(x),u.add(x)
    for(let i in a[x])if(!r[n].has(a[x][i]))dfs(a[x][i])
  }
  for(let i in a){
    r.push(new Set())
    dfs(i)
    if(!r[r.length-1].size)r.pop()
  }
  return r.length
}

