Number.prototype.add=function(x){
  return this+x
}
Number.prototype.subtract=function(x){
  return this-x
}
Number.prototype.multiply=function(x){
  return this*x
}
Number.prototype.divide=function(x){
  return this/x
}
Number.prototype.square=function(x){
  return this**2
}
